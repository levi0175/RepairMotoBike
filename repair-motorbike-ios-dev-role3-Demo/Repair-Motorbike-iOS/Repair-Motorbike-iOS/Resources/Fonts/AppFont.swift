import UIKit

struct AppFont {
    struct T12 {
        static let Medium = UIFont(name: "Baloo2-Medium", size: 12)
        static let Regular = UIFont(name: "Baloo2-Regular", size: 12)
    }

    struct T14 {
        static let Medium = UIFont(name: "Baloo2-Medium", size: 14)
        static let Regular = UIFont(name: "Baloo2-Regular", size: 14)
    }

    struct T16 {
        static let Medium = UIFont(name: "Baloo2-Medium", size: 16)
        static let Regular = UIFont(name: "Baloo2-Regular", size: 16)
    }

    struct T18 {
        static let SemiBold = UIFont(name: "Baloo2-SemiBold", size: 18)
        static let Medium = UIFont(name: "Baloo2-Medium", size: 18)
        static let Regular = UIFont(name: "Baloo2-Regular", size: 18)
    }

    struct T20 {
        static let SemiBold = UIFont(name: "Baloo2-SemiBold", size: 20)
        static let Medium = UIFont(name: "Baloo2-Medium", size: 20)
        static let Regular = UIFont(name: "Baloo2-Regular", size: 20)
    }

    struct T24 {
        static let Regular = UIFont(name: "Baloo2-Regular", size: 24)
    }

    struct T30 {
        static let SemiBold = UIFont(name: "Baloo2-SemiBold", size: 30)
    }
}
